import type { RagChunkRepository } from "../repositories/RagChunkRepository";
import type { EmbeddingClient } from "../llm/EmbeddingClient";

// Для прайсов/каталогов факты разбросаны по многим чанкам, и конкретный товар часто
// не попадает в топ-3 (эмбеддинг «многотоварного» чанка размыт). Берём с запасом.
const TOP_K = 8;

// Порог косинусной дистанции: чанки дальше него считаем нерелевантными и НЕ подклеиваем.
// Замерено: реальные вопросы дают ближайший чанк ≤0.72 («фильтр» 0.50, «бампер» 0.71),
// а служебные реплики — ≥0.75 («да» 0.80, «нет» 0.82, «2016» 0.83). 0.73 их разделяет.
const MAX_DISTANCE = 0.73;

export class RagSearchManager {
  constructor(
    private chunkRepo: RagChunkRepository,
    private embeddingClient: EmbeddingClient
  ) {}

  // Возвращает готовый текстовый блок для подклейки в system prompt, либо null, если ничего не нашли
  async buildContext(botId: number, queryText: string): Promise<string | null> {
    const queryEmbedding = await this.embeddingClient.embed(queryText);
    const chunks = await this.chunkRepo.searchByBot(botId, queryEmbedding, TOP_K, MAX_DISTANCE);
    if (chunks.length === 0) return null;

    // Данные обрамлены и снабжены ПРИОРИТЕТНЫМ правилом в конце (модель читает его последним,
    // прямо перед ответом). Оно разрешает конфликт «защита цели (откажи)» vs «ответь из RAG»:
    // вопрос, ответ на который есть в данных, ВСЕГДА считается по теме → сначала ответить
    // конкретикой, потом вернуться к цели шага. Проверено — стабильно даже на слабой модели.
    return `Контекст из базы знаний (справочные данные):
${chunks.map((c) => `- ${c.content}`).join("\n")}
=== ПРИОРИТЕТНОЕ ПРАВИЛО ===
Если последнее сообщение клиента — вопрос, и ответ на него ЕСТЬ в данных выше: это ВСЕГДА считается вопросом по теме (НЕ отказывай «не помогу»). ОБЯЗАТЕЛЬНО сначала коротко ответь конкретикой из данных выше (наличие/цена/условие), и в том же bot_message затем вернись к вопросу своего шага. Если ответа в данных выше НЕТ — не выдумывай и не ссылайся на менеджера: честно скажи, что таких данных в нашей базе нет, и продолжай свою задачу. Не здоровайся повторно если greeted=true. Отвечай строгим JSON.`;
  }
}
